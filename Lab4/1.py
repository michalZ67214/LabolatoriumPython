lista_zakupow = ['banan', 'jabłko', 'masło', 'chleb']

print(lista_zakupow)

# wyświetl pierwszy i drugi element z listy
print(lista_zakupow[0] + ', ' + lista_zakupow[1])

# zmień trzeci element listy na „mleko”
lista_zakupow[2] = 'mleko'

print(lista_zakupow)

# wyświetl przedostatni i ostatni element listy
print(lista_zakupow[-2] + ' ,' + lista_zakupow[-1])